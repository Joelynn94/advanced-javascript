/*
  Interpreters run through a program line by line and translates the program into machine code. Interpreted languages were once significantly slower than compiled languages. But, with the development of just-in-time compilation, that gap is shrinking.
  -PHP, Ruby, Python, and JavaScript.

  Compiled languages are converted directly into machine code that the processor can execute. As a result, they tend to be faster and more efficient to execute than interpreted languages. They also give the developer more control over hardware aspects, like memory management and CPU usage.
  -C, C++, Erlang, Haskell, Rust, and Go.

  Babel is a Javascript compiler that takes your modern JS code and returns  browser compatible JS (older JS code).
  Typescript is a superset of Javascript that compiles down to Javascript.

  Both of these do exactly what compilers do: Take one language and convert into a different one!
*/